let x, y, result;
let sumOutput, differenceOutput, productOutput, quotientOutput, rmOutput;

x = Math.floor(Math.random() * 100 +1);
y = Math.floor(Math.random() * 50 +1);

// Sum Output
result = x + y;
sumOutput = `${x} + ${y} = ${result}`;
console.log('sumOutput', sumOutput);

// Difference Output
result = x - y;
differenceOutput = `${x} - ${y} = ${result}`;
console.log('differenceOutput', differenceOutput);

// Product Output
result = x * y;
productOutput = `${x} * ${y} = ${result}`;
console.log('productOutput', productOutput);

// quotient Output
result = x / y;
quotientOutput = `${x} / ${y} = ${result}`;
console.log('quotientOutput', quotientOutput);

// Remainder Output
result = x % y;
rmOutput = `${x} % ${y} = ${result}`;
console.log('rmOutput', rmOutput);


